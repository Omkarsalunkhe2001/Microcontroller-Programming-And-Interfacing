#define SHT3_ADDRESS_WRITE (0x70 << 1)                   // Write address for SHT3 sensor
#define SHT3_ADDRESS_READ  ((0x70 << 1) | 0x01)          // Read address for SHT3 sensor

  uint8_t regHum[2];                                     // Command for humidity
  uint8_t rawData[6];                                    // Buffer for raw data (temperature + humidity)
  uint16_t rawHum16;                                     // Raw humidity data
  float humidity;                                        // Converted humidity value
  char printBuff[64] = {0};
  regHum[0] = 0x7C;                                      // Command MSB
